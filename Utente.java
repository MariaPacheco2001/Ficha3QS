public class Utente {


    private String nome;
    private int idade;
    private String genero;
    private double altura;
    private double peso;

    public Utente(String nome, int idade, String genero, double altura, double peso) {
        this.nome = nome;
        this.idade = idade;
        this.genero = genero;
        this.altura = altura;
        this.peso = peso;

    }
}
